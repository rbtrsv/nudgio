"""
Public Widget Subrouter

Public endpoints for storefront widget delivery via HMAC-signed URLs.
Used by non-Shopify platforms (WooCommerce, Magento, custom sites).

Storefront URL pattern (generated by WordPress plugin / JS snippet):
    https://server.nudgio.tech/ecommerce/widget/{type}?key_id=5&ts=1709913600&nonce=a1b2c3&top=4&style=card&sig=<HMAC>

Security:
- HMAC-SHA256 signature covers ALL query params (prevents tampering)
- Timestamp + nonce prevent replay attacks
- Domain restriction as secondary signal (defense-in-depth)
- Dedicated rate limiting (per key_id + per IP)

Endpoints:
- GET /widget/bestsellers — bestseller widget HTML
- GET /widget/cross-sell — cross-sell widget HTML (requires product_id)
- GET /widget/upsell — upsell widget HTML (requires product_id)
- GET /widget/similar — similar products widget HTML (requires product_id)

Auth: HMAC-signed URLs (NOT JWT, NOT raw API key in query string).
All responses are HTMLResponse (renders inside iframe on merchant's storefront).
"""

import logging

from fastapi import APIRouter, Depends, Query, Request
from fastapi.responses import HTMLResponse
