# GET /bestsellers — Bestseller Widget
# ==========================================

@router.get("/bestsellers", response_class=HTMLResponse)
async def get_bestsellers_widget(
    request: Request,
    key_id: int = Query(..., description="Widget API key ID (public identifier)"),
    ts: int = Query(..., description="Unix timestamp (request time)"),
    nonce: str = Query(..., description="Random string per request (prevents replay)"),
    sig: str = Query(..., description="HMAC-SHA256 signature of canonical query string"),
    top: int = Query(4, description="Number of recommendations to show"),
    lookback_days: int = Query(30, description="Number of days to look back for order data"),
    method: str = Query("volume", description="Bestseller calculation method: volume, value, or balanced"),
    style: str = Query("card", description="Component style: card, carousel"),
    device: str = Query("desktop", description="Target device: desktop, mobile"),
    columns: int = Query(4, description="Max columns at full width (2–6)"),
    size: str = Query("default", description="Visual density: compact, default, spacious"),
    primary_color: str = Query("#3B82F6", description="Primary color hex"),
    text_color: str = Query("#1F2937", description="Text color hex"),
    bg_color: str = Query("#FFFFFF", description="Background color hex"),
    border_radius: str = Query("8px", description="Border radius"),
    db: AsyncSession = Depends(get_session),
):
    """
    Serve bestseller recommendation widget HTML via HMAC-signed URL.

    Public endpoint — no JWT, no session token. Authenticated via HMAC
    signature covering all query parameters.

    This endpoint:
    1. Authenticate request (HMAC + domain + rate limit + entitlement)
    2. Get RecommendationSettings for shop URL configuration
    3. Create adapter + engine, check cache or generate bestseller recommendations
    4. Generate HTML widget via generate_recommendation_html
    5. Return HTMLResponse for iframe rendering
    """
    try:
        # Step 1: Authenticate
        api_key, connection = await _authenticate_widget_request(request, db)

        # Step 2: Get shop URL settings
        settings_result = await db.execute(
            select(RecommendationSettings).where(
                RecommendationSettings.connection_id == connection.id
            )
        )
        rec_settings = settings_result.scalar_one_or_none()
        shop_urls = get_default_shop_urls(connection, rec_settings)

        # Step 3: Create adapter + engine, check cache or generate
        adapter = get_adapter(connection, db)
        engine = RecommendationEngine(adapter)

        cached = await get_cached_recommendations(
            connection.id, "bestseller",
            limit=top, method=method, lookback_days=lookback_days,
        )
        if cached is not None:
            recs = cached
        else:
            method_enum = BestsellerMethod(method) if method in [m.value for m in BestsellerMethod] else BestsellerMethod.VOLUME
            recs = await engine.get_bestsellers(limit=top, lookback_days=lookback_days, method=method_enum)
            await set_cached_recommendations(
                connection.id, "bestseller", recs,
                limit=top, method=method, lookback_days=lookback_days,
            )

        # Step 4: Generate HTML widget
        html = generate_recommendation_html(
            recommendations=recs,
            style=style,
            device=device,
            columns=columns,
            size=size,
            colors={"primary": primary_color, "text": text_color, "bg": bg_color},
            border_radius=border_radius,
            rec_type="bestseller",
            shop_urls=shop_urls,
        )

        # Step 5: Return HTMLResponse
        return HTMLResponse(content=html)

    except WidgetAuthError as e:
        logger.warning(f"Widget bestsellers auth error: {str(e)}")
        status_code = 429 if "rate limit" in str(e).lower() else 403
        return HTMLResponse(content=_error_html(str(e)), status_code=status_code)
    except Exception as e:
        logger.error(f"Widget bestsellers error: {str(e)}")
        return HTMLResponse(content=_error_html("Error loading recommendations"), status_code=500)


