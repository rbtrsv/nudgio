router = APIRouter(prefix="/widget", tags=["Public Widget"])


# ==========================================
# Error HTML Templates
# ==========================================

def _error_html(message: str) -> str:
    """Generate minimal error HTML for iframe display."""
    return f"""<!DOCTYPE html>
<html>
<head><meta charset="UTF-8"></head>
<body style="display:flex;align-items:center;justify-content:center;min-height:100px;font-family:sans-serif;color:#6B7280;">
    <p>{message}</p>
</body>
</html>"""


# ==========================================
# Shared Auth + Rate Limit Flow
# ==========================================

async def _authenticate_widget_request(
    request: Request, db: AsyncSession
) -> tuple:
    """
    Common authentication flow for all widget endpoints.

    This function:
    1. Verify HMAC signature via verify_widget_signature
    2. Check domain restriction (secondary signal)
    3. Check dedicated rate limit (per key_id + per IP)
    4. Check entitlement via is_service_active
    5. Return (api_key, connection)

    Raises:
        WidgetAuthError on auth failure
        Returns HTMLResponse tuple (content, status_code) on non-auth failure
    """
    # Step 1: Verify HMAC signature
    api_key, connection = await verify_widget_signature(dict(request.query_params), db)

    # Step 2: Check domain restriction (secondary signal — defense-in-depth)
    referer = request.headers.get("referer")
    if not check_domain_restriction(api_key, referer):
        logger.warning(f"Widget: domain restriction failed for key_id={api_key.id}, referer={referer}")
        raise WidgetAuthError("Domain not allowed")

    # Step 3: Check dedicated rate limit
    client_ip = request.client.host if request.client else "unknown"
    rate_ok = await check_widget_rate_limit(api_key.id, client_ip)
    if not rate_ok:
        raise WidgetAuthError("Rate limit exceeded")

    # Step 4: Check entitlement (active subscription or free tier)
    service_active = await is_service_active(connection.organization_id, db)
    if not service_active:
        logger.info(f"Widget: service inactive for key_id={api_key.id}, org_id={connection.organization_id}")
        raise WidgetAuthError("Widget unavailable — subscription required")

    return api_key, connection


